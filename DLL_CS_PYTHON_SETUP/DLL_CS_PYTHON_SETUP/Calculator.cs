using System;

namespace Calculate
{
    public class Calculator
    {
        public double Add(double x, double y)
        {
            return x + y;
        }
        public double Substract(double x, double y)
        {
            return x - y;
        }
        public double Multiply(double x, double y)
        {
            return x * y;
        }
        public double Devide(double x, double y)
        {
            return x / y;
        }
    }
}
